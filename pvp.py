    font = pygame.font.Font(None, 50)

    while running:
        screen.fill(pygame.Color('grey'))
        screen.blit(frame, (0, 0))

        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.mouse.set_visible(True)
                return 1, None

            if event.type == MYEVENTTYPE:
                if tank.max_ammunition != tank.ammunition:
                    tank.ammunition += 1
                if tank2.max_ammunition != tank2.ammunition:
                    tank2.ammunition += 1

            elif event.type == pygame.KEYDOWN:
                if event.key == pygame.K_w:
                    now_press1 = event.key
                elif event.key == pygame.K_a:
                    now_press1 = event.key
                elif event.key == pygame.K_s:
                    now_press1 = event.key
                elif event.key == pygame.K_d:
                    now_press1 = event.key

                if event.key == pygame.K_UP:
                    now_press2 = event.key
                if event.key == pygame.K_LEFT:
                    now_press2 = event.key
                if event.key == pygame.K_DOWN:
                    now_press2 = event.key
                if event.key == pygame.K_RIGHT:
                    now_press2 = event.key

            elif event.type == pygame.KEYUP:
                if event.key == now_press1:
                    now_press1 = 0
                if event.key == now_press2:
                    now_press2 = 0

                if event.key == pygame.K_SPACE:
                    button = True
                if event.key == pygame.K_l:
                    button2 = True

        if now_press1 == 100:  # D
            tank.move_west()
        elif now_press1 == 115:  # S
            tank.move_south()
        elif now_press1 == 97:  # A
            tank.move_east()
        elif now_press1 == 119:  # W
            tank.move_north()

        if now_press2 == 1073741903:  # D
            tank2.move_west()
        elif now_press2 == 1073741905:  # S
            tank2.move_south()
        elif now_press2 == 1073741904:  # A
            tank2.move_east()
        elif now_press2 == 1073741906:  # W
            tank2.move_north()

        if button:  # рисование пули
            tank.shot()
            sound1.play()
            button = False

        if button2:  # рисование пули
            tank2.shot()
            sound1.play()
            button2 = False

        create_map(screen)
        if tank.hp > 0:
            screen.blit(tank.tank, tank.get_pos())
        else:
            pygame.mouse.set_visible(True)
            return 7, 2
        if tank2.hp > 0:
            screen.blit(tank2.tank, tank2.get_pos())
        else:
            pygame.mouse.set_visible(True)
            return 7, 1

        all_sprites.draw(screen)
        all_sprites.update()

        create_leaves(screen)
        text = font.render(str(tank2.hp) + '/' + str(tank2.max_hp), True, pygame.Color('red'))
        screen.blit(text, (100, 10))
        text = font.render(str(tank.hp) + '/' + str(tank.max_hp), True, pygame.Color('red'))
        screen.blit(text, (100, 660))

        text = font.render(str(tank.ammunition) + '/' + str(tank.max_ammunition), True, pygame.Color('brown'))
        screen.blit(text, (300, 660))
        text = font.render(str(tank2.ammunition) + '/' + str(tank2.max_ammunition), True, pygame.Color('brown'))
        screen.blit(text, (300, 10))

        pygame.display.flip()
        clock.tick(fps)
        pygame.mouse.set_visible(False)

        pygame.display.flip()
